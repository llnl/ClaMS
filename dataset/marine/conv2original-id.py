"""
Convert sequential IDs back to original IDs.
The ID mapping table is generated by another script.

python3 [this script].py \
  --mapping marine_mapping.tsv \
  --input cluster_output.txt \
  --output cluster_output_with_orig_id.txt
"""

#!/usr/bin/env python3

import argparse


def load_mapping(mapping_path: str) -> dict[int, str]:
    """Load seq_id -> orig_id mapping."""
    mapping = {}
    with open(mapping_path, "r", encoding="utf-8") as f:
        header = f.readline()  # skip mapping header
        for line_no, line in enumerate(f, start=2):
            parts = line.rstrip("\n").split("\t")
            if len(parts) < 4:
                raise ValueError(f"Malformed mapping at line {line_no}")
            seq_id = int(parts[0])
            orig_id = parts[3]
            mapping[seq_id] = orig_id
    return mapping


def main():
    ap = argparse.ArgumentParser(
        description="Replace sequential IDs with original IDs using marine_mapping.tsv"
    )
    ap.add_argument("--mapping", required=True, help="marine_mapping.tsv")
    ap.add_argument("--input", required=True, help="Input file to convert")
    ap.add_argument("--output", required=True, help="Output file")

    args = ap.parse_args()

    if args.input == args.output:
        raise ValueError("Input and output files must be different")

    seq_to_orig = load_mapping(args.mapping)

    with open(args.input, "r", encoding="utf-8") as fin, open(
        args.output, "w", encoding="utf-8"
    ) as fout:
        # --- always write the first line as header ---
        header = fin.readline()
        if header:
            fout.write(header)

        # --- process remaining lines ---
        for line_no, line in enumerate(fin, start=2):
            stripped = line.strip()
            if not stripped:
                fout.write(line)
                continue

            parts = stripped.split()
            try:
                seq_id = int(parts[0])
            except ValueError:
                raise ValueError(
                    f"Expected integer seq_id in first column at line {line_no}"
                )

            if seq_id not in seq_to_orig:
                raise KeyError(
                    f"seq_id {seq_id} not found in mapping (line {line_no})"
                )

            parts[0] = seq_to_orig[seq_id]
            fout.write("\t".join(parts) + "\n")


if __name__ == "__main__":
    main()
