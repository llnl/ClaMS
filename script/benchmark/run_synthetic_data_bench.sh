#!/bin/bash

# This script runs the benchmark for synthetic data (knng-hdbscan/dataset/synthetic)
# Usage example:
# cd knng-hdbscan
# cd dataset/synthetic
# # Gnerate synthetic data using the generator
# cd ../../build
# sh ../script/run_synthetic_data_bench.sh -m 15 -s 10 -d blobs

# Must be one of the dataset names in knng-hdbscan/dataset/synthetic
DATASET="blobs"
MIN_CLUSTER_SIZE=15
# MIN_SAMPLES must be <= MIN_CLUSTER_SIZE
MIN_SAMPLES=10

BATCH_OPTIONS="-t 10:00 -B clmshls"

# Check if we are on allocated node
if [ -z "${SLURM_JOB_ID+x}" ]; then
  # Prompt the user (default is “no”)
  read -r -p "Warning: Not running on an allocate node. Continue anyway? [y/N] " answer
  # If they didn’t answer “y” or “Y”, abort
  if [[ ! $answer =~ ^[Yy]$ ]]; then
    echo "Aborting."
    exit 1
  fi
fi

# Parse command line arguments
while getopts "d:m:s:B:" opt; do
  case ${opt} in
  d )
    DATASET=$OPTARG
    ;;
  m )
    MIN_CLUSTER_SIZE=$OPTARG
    ;;
  s )
    MIN_SAMPLES=$OPTARG
    ;;
  B )
    BATCH_OPTIONS=$OPTARG # e.g.,) -B "-- -t10:00 -Aclmshls"
    ;;
  \? )
    echo "Usage: cmd [-m min cluster size] [-s min samples] [-d dataset] [-B batch job options]"
    ;;
  esac
done

OUT_DIR="./out_synthetic_data_bench/m${MIN_CLUSTER_SIZE}_s${MIN_SAMPLES}/${DATASET}/"
WORK_DIR="${OUT_DIR}/work/"
POINT_FILE="../../dataset/synthetic/${DATASET}_points.txt"
LABEL_FILE="../../dataset/synthetic/${DATASET}_labels.txt"

#rm -rf ${OUT_DIR}
mkdir -p ${OUT_DIR}
mkdir -p ${WORK_DIR}

function set_mst_out_path() {
  local algo=$1
  MST_OUT_PATH="${OUT_DIR}/mst_${algo}.txt"
}

function set_cluster_out_path() {
  local algo=$1
  CLUSTER_OUT_PATH="${OUT_DIR}/clusters_${algo}.txt"
}

function set_mst_plot_path() {
  local algo=$1
  MST_PLOT_PATH="${OUT_DIR}/mst_${algo}.pdf"
}

function set_cluster_plot_path() {
  local algo=$1
  CLUSTER_PLOT_PATH="${OUT_DIR}/clusters_${algo}.pdf"
}

# Run Kruskal MST
# Output: EXACT_MST_FILE
function run_exact_mst() {
  local knn_dir=$1
  local name=$2

  # Run kruskal_mst
  local mst_dir="${WORK_DIR}/kruskal_mst"
  mkdir -p ${mst_dir}
  rm -f "${mst_dir}/*"
  ./src/mst/run_kruskal_mst ${knn_dir} ${mst_dir}

  # Merge all MST files into one
  EXACT_MST_FILE="${OUT_DIR}/exact_mst_${name}.txt"
  rm -f ${EXACT_MST_FILE}
  cat ${mst_dir}/* >> ${EXACT_MST_FILE}
}

# -----------------------------------------
# Ground Truth Clustering Plot
# -----------------------------------------
set_cluster_plot_path "gt"
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${LABEL_FILE} -o ${CLUSTER_PLOT_PATH}

# -----------------------------------------
# Run HDBSCAN
# -----------------------------------------
set_mst_out_path "hdbscan"
set_cluster_out_path "hdbscan"
set_mst_plot_path "hdbscan"
set_cluster_plot_path "hdbscan"

python3 ../script/run_hdbscan.py -p ${POINT_FILE} -m ${MIN_CLUSTER_SIZE} -s ${MIN_SAMPLES} -o ${CLUSTER_OUT_PATH} -M ${MST_OUT_PATH} -g ${LABEL_FILE}

# HDBSCAN MST Plot
python3 ../script/plot_mst.py -p ${POINT_FILE} -m ${MST_OUT_PATH} -o ${MST_PLOT_PATH}

# HDBSCAN Clustering Plot
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${CLUSTER_OUT_PATH} -o ${CLUSTER_PLOT_PATH}


# -----------------------------------------
# Our HDBSCAN with MST generated by HDBSCAN
# -----------------------------------------
# MST_OUT_PATH is already set by the previous HDBSCAN run
set_cluster_out_path "clms_hdbscan"
set_cluster_plot_path "clms_hdbscan"

./src/run_hdbscan_clustering -i ${MST_OUT_PATH} -m ${MIN_CLUSTER_SIZE} -o ${CLUSTER_OUT_PATH} -P

# HDBSCAN Clustering Plot
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${CLUSTER_OUT_PATH} -o ${CLUSTER_PLOT_PATH}


# -----------------------------------------
# Run KNN HDBSCAN Clustering (clams clustering)
# -----------------------------------------
CMSL_BATCH_LOG="${WORK_DIR}/log_clms_batch.txt"
python3 ../script/run_clams_bench.py -p ${POINT_FILE} -m ${MIN_CLUSTER_SIZE} -k ${MIN_SAMPLES} -g ${LABEL_FILE} -w ${WORK_DIR} -B "$BATCH_OPTIONS" | tee ${CMSL_BATCH_LOG}
# sleep 30 # Wait for the batch to finish

#out_dir=$(grep "Output directory: ${CMSL_BATCH_LOG}" | awk '{print $3}')
CLMS_WORK_DIR=$(grep "Work directory:" ${CMSL_BATCH_LOG} | awk '{print $3}')
CLMS_CLUSTER_PATH="${CLMS_WORK_DIR}/cluster_labels-try0.txt"
AMST_FILE="${CLMS_WORK_DIR}/mst/mst.txt"
KNN_DIR="${CLMS_WORK_DIR}/knng_merged/"

# Execute the generated batch script on this node
job_script=$(grep "Generated batch script:" ${CMSL_BATCH_LOG} | awk '{print $4}')
bash ${job_script}
# Wait for the back to finish

# Remove the first line
sed -i '1d' ${CLMS_CLUSTER_PATH}

# KNN HDBSCAN Clustering Plot
set_cluster_out_path "clms"
cp ${CLMS_CLUSTER_PATH} ${CLUSTER_OUT_PATH}
set_cluster_plot_path "clms"
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${CLUSTER_OUT_PATH} -o ${CLUSTER_PLOT_PATH}

# AMST Plot
set_mst_out_path "amst"
cp ${AMST_FILE} ${MST_OUT_PATH}
set_mst_plot_path "amst"
python3 ../script/plot_mst.py -p ${POINT_FILE} -m ${MST_OUT_PATH} -o ${MST_PLOT_PATH}


# -----------------------------------------
# KNN HDBSCAN with Kruskal MST
# -----------------------------------------
set_cluster_out_path "clms_exact_mst"
set_mst_plot_path "clms_exact_mst"
set_cluster_plot_path "clms_exact_mst"

# Run kruskal_mst
run_exact_mst ${KNN_DIR} "original_dist"

# Kruskal MST Plot
set_mst_out_path "clms_exact_mst"
cp ${EXACT_MST_FILE} ${MST_OUT_PATH}
python3 ../script/plot_mst.py -p ${POINT_FILE} -m ${MST_OUT_PATH} -o ${MST_PLOT_PATH}

# Run KNN HDBSCAN Clustering with Kruskal MST
./src/run_hdbscan_clustering -i ${MST_OUT_PATH} -m ${MIN_CLUSTER_SIZE}  -o ${CLUSTER_OUT_PATH} -P

# KNN HDBSCAN Kruskal-MST Clustering Plot
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${CLUSTER_OUT_PATH} -o ${CLUSTER_PLOT_PATH}

# -----------------------------------------
# KNN with Mutual Reachability Distance
# -----------------------------------------
MR_DIST_FILE="${OUT_DIR}/mreach_dist.txt"
./src/conv_knng_to_mreach_dist -i ${KNN_DIR} -o ${MR_DIST_FILE} -m ${MIN_SAMPLES}

run_exact_mst ${MR_DIST_FILE} "mreach_dist"

set_mst_out_path "clms_mreach_dist"
cp ${EXACT_MST_FILE} ${MST_OUT_PATH}
set_mst_plot_path "clms_mreach_dist"
python3 ../script/plot_mst.py -p ${POINT_FILE} -m ${MST_OUT_PATH} -o ${MST_PLOT_PATH}

# Run KNN HDBSCAN Clustering with Mutual Reachability Distance
set_cluster_out_path "clms_mreach_dist"
./src/run_hdbscan_clustering -i ${MST_OUT_PATH} -m ${MIN_CLUSTER_SIZE} -o ${CLUSTER_OUT_PATH} -P

# KNN HDBSCAN Mutual Reachability Distance Clustering Plot
set_cluster_plot_path "clms_mreach_dist"
python3 ../script/plot_cluster_data.py -p ${POINT_FILE} -l ${CLUSTER_OUT_PATH} -o ${CLUSTER_PLOT_PATH}
